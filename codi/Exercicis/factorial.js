

num=Number(prompt('Dame un número:'));

factorial=1;

// For loop
for(i=num; i>0; i--){
    factorial=factorial*i;
}
document.write(factorial);



// While loop

num2 = Number(prompt('Dame un número:'));
factorial = 1

while (num2 > 1){
    factorial = factorial * num2
    num2 = num2 - 1;}
    document.write(factorial);
