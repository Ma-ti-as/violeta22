//bucle while

let cont = 0;
while (cont <= 10) {
  console.log(`repetición número:  ${cont}`);
  cont++;
}
console.log(`Valor final: ${cont}`);

// bucle for

let cont2;
for (cont2 = 0; cont2 <= 10; cont2++) {
  console.log(`Iteración número: ${cont2}`);
}
console.log(`Valor iteración final: ${cont2}`);

// bucle do while
/* cont = 0; */
do {
  console.log(`Repetición en Do while número:  ${cont}`);
  cont++;
} while (cont <= 10);
console.log(`Valor Do while final: ${cont}`);
